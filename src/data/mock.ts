function generateMetrics(baseLine: number, variance: number, unit: string, count = 20): EquipmentMetric[] {
  const now = Date.now();
  return Array.from({ length: count }, (_, i) => ({
    metric_id: `metric-${Math.random().toString(36).slice(2, 8)}`,
    value: Math.round((baseLine + (Math.random() - 0.5) * variance * 2) * 100) / 100,
    unit_symbol: unit,
    created_at: new Date(now - (count - 1 - i) * 30_000).toISOString(),
  }));
}

